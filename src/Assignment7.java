
/*
 * 7.	Write a class named Assignment7 which uses a for-loop to print only the even numbers between 0 and 10, inclusive. 
 */

public class Assignment7 {
	
	public static void main(String[] args) {
		
		int count = 0;
		for (count = 0; count <= 10; count++) {
			System.out.println(count);
		}
	}
}
